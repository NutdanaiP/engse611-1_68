// ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞ field
function updateFieldStatus(input, isValid, message) {
    const errorDiv = document.getElementById(`${input.id}Error`);
    errorDiv.textContent = message;
    errorDiv.style.color = isValid ? '' : 'var(--error-color)';
    input.style.borderColor = isValid ? '' : 'var(--input-invalid-border)';
    input.classList.remove(isValid ? 'invalid' : 'valid');
    input.classList.add(isValid ? 'valid' : 'invalid');
}

async function submitContactForm() {
    if (isSubmitting) return;
    
    const formData = new FormData(contactForm);
    const data = Object.fromEntries(formData.entries());
    
    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö client-side validation
    for (const [key, value] of Object.entries(data)) {
        const { isValid, message } = validateField(key, value);
        if (!isValid) {
            showStatusMessage(`üî∏ ${message}`, 'error');
            updateFieldStatus(document.getElementsByName(key)[0], isValid, message);
            return;
        }
    }
    
    try {
        isSubmitting = true;
        updateSubmitButton('contactSubmit', '‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏™‡πà‡∏á...', true);
        
        const response = await fetch('/api/contact', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(data)
        });
        
        const result = await response.json();
        
        if (result.success) {
            showStatusMessage('‚úÖ ‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à! ‡πÄ‡∏£‡∏≤‡∏à‡∏∞‡∏ï‡∏¥‡∏î‡∏ï‡πà‡∏≠‡∏Å‡∏•‡∏±‡∏ö‡πÇ‡∏î‡∏¢‡πÄ‡∏£‡πá‡∏ß', 'success');
            contactForm.reset();
            contactForm.querySelectorAll('.error').forEach(div => div.textContent = '');
            contactForm.querySelectorAll('input, textarea').forEach(input => {
                input.classList.remove('valid', 'invalid');
                input.style.borderColor = '';
            });
        } else {
            showStatusMessage(`‚ùå ‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î: ${result.message}`, 'error');
            if (result.errors) {
                displayValidationErrors(result.errors);
            }
        }
    } catch (error) {
        showStatusMessage('‚ùå ‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠', 'error');
        console.error('Error:', error);
    } finally {
        isSubmitting = false;
        updateSubmitButton('contactSubmit', '‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°', false);
    }
}

